ll gcd(ll a,ll b){if(b==0)return a; return gcd(b,a%b);}
ll lcm(ll a,ll b){return (a/gcd(a,b))*b;}

void solve(){
    string s;
    cin>>s;
    int z=0,o=0;
    for(auto& i:s){
        if(i=='1')o++;
        else z++;
    }
    if(z==o)cout<<z-1<<"\n";
    else cout<<min(z,o)<<'\n';
} 

